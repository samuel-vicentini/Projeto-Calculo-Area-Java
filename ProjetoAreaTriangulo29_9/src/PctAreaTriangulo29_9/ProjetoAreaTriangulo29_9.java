    private void btnVerificarTrianguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarTrianguloActionPerformed
        // TODO add your handling code here:
        float Lado1, Lado2, Lado3;
        String resultado;
        try
        {
            lblErro2.setText(String.valueOf(""));
            Lado1 = Float.parseFloat(tfLado1.getText());
            Lado2 = Float.parseFloat(tfLado2.getText());
            Lado3 = Float.parseFloat(tfLado3.getText());
            if (Lado1 == Lado2 && Lado1 == Lado3)
            {
                resultado = "Equilátero";
            }
            else if (Lado1 == Lado2 || Lado2 == Lado3 || Lado1 == Lado3)
            {
                resultado = "Isósceles";
            }
            else 
            {
                resultado = "Escaleno";
            }
            lblTipoTriangulo.setText(String.valueOf(resultado));
        }
        catch(NumberFormatException e)
        {
            lblErro2.setText(String.valueOf("[erro] Campo vazio ou Caracter inválido!"));
            lblTipoTriangulo.setText(String.valueOf(""));
            tfLado1.setText(String.valueOf(""));
            tfLado2.setText(String.valueOf(""));
            tfLado3.setText(String.valueOf(""));
            tfLado1.requestFocus();
        }
         
    }//GEN-LAST:event_btnVerificarTrianguloActionPerformed

