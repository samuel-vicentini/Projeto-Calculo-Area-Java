    private void btnCalcularAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularAreaActionPerformed
        // TODO add your handling code here:
        double area, altura, base;
        try
        {
            lblErro.setText(String.valueOf(""));
            base = Double.parseDouble(tfBase.getText());
            altura = Double.parseDouble(tfAltura.getText());
            area = altura*base;
            lblArea.setText(String.valueOf(area));
        }
        catch(NumberFormatException e)
        {
            lblErro.setText(String.valueOf("[erro] Campo vazio ou Caracter inválido!"));
            tfBase.setText(String.valueOf(""));
            tfAltura.setText(String.valueOf(""));
            lblArea.setText(String.valueOf(""));
            tfBase.requestFocus();
        }
        
        
    }//GEN-LAST:event_btnCalcularAreaActionPerformed

